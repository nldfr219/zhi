function show_datetime($datetime)
{   
    // for caches delays
    // ------------------------
    // from yyyy-mm-dd hh:mn:ss
    // to      mm-yyyy hh:mn:ss

    $date  = substr($datetime, 8, 2) . '/'; // day
    $date .= substr($datetime, 5, 2) . ' '; // month
    $date .= substr($datetime, 11, 5);      // time

    return($date);
}

